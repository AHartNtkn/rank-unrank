import {digit, undigit} from './digit.js'

BigInt.prototype.toJSON = function() { return this.toString()  }

test('Verifying for low values that digit and undigit are inverses.', () => {
  for (let i = 2n; i < 100n; i++) {
  for (let j = 0n; j < 100n; j++) {
    expect(
      undigit(i, digit(i, j)) ).toBe(
      j
    )
  }}
})

